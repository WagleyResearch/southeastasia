    <h2>British in Afghanistan</h2>
    <p>One of Britain's major exports from India was opium, and by 1770 they had a manopoly on the product, and started to also grow it in Afghanistan. In 1839, Britain send 12,000 troops to the country, to remove the current king, Dost Mohammad, from power because they suspected him of being too friendly with Russia, which may have endangered their drug trade.</p>
